        <h2>Historie sboru dobrovolných hasičů Radíkov od založení v roce 1892 do roku 2002</h2>

        <p><b>Založení sboru - výtah z dobového zápisu:</b></p>

        <p><cite>„Vyskytla se myšlenka obecenstvu zdejší obce, zdališ by se nemohla zhodnot Jednota hasičská,
            pod jménem Dobrovolná jednota hasičská v Radíkově. Věci té se ujali následující občané a zvolali
            obecenstvo na den 11. září 1892 do hostince pana Františka Melcera a dostavili se všechny osoby mužského pohlaví.
                Promluvil o potřebách pan Karel Smékal, t.č. starosta obce a předpověděl dopis vybídnutí slavným C.K.
            okresním hejtmanstvím v Olomouci ze dne 24. února 1892.
                Stal se rozhovat o hlasování, zdališ jednota dle členů utvrzená býti může a sepsaná byla presentační
            listina a předložena k veřejnému podpisu . to se po sumě k podepsané listině
            shledalo pro jednotu počet postačí.“</cite></p>

        <p>Stanovy byly vypůjčeny a schváleny z Velké Bystřice. Byl ustanoven první prozatimní výbor. Každý člen
            musel dát do pokladny přístupné 1 zlatý, jak stanova § 5 předepisuje.</p>

        <p>Sbor měl k dispozici dvoukolovou ruční tzv.sundávací stříkačku, taženou koňmi, 100 m hadic, 4 kusy žebříků,
            1 berlovku, 2 pochodně, 2 svítilny, 2 sekery, 2 krumpáče, 2 lopaty, 2 vidle vše v ceně 1600 zlotých.</p>

        <p>To vše bylo uskladněno ve zbrojnici, postavené svépomocí roku 1892 z nepálených cihel vepřovic, které si sami
            nadělali z hlíny. Na zednické, tesařské práce a jiný materiál se půjčili peníze ze záložny na Kopečku.
            Za úvěr ručili sami členové sboru. Výpůjčka se splácela z příjmů ze zábav a dobrovolných darů a ze sbírky po obci.
            Veškerá výzbroj a výstroj se pořizovala ze stejných příjmů. Protože členové věděli, jak těžko se pořizuje výzbroj,
            udržovali vše ve vzorném pořádku. Co bylo z mosazi, každý čistil Sidolem, vše se jen blýskalo.
            Železné věci se nechali niklovat v Mariánském údolí.</p>

        <p><b>Mezi záslužnou činnost zejména patřilo a patří:</b></p>
        <ul>
            <li>Zásahy pří požárech /největší viz. historie/</li>
            <li>Zásahy při povodních a to zejména roku 1914 v Radíkově, 1981 a 1997 v Olomouci</li>
            <li>Preventiví činnost, každý rok byly až do roku 1990 prováděny preventivní prohlídky domů s důrazem na komíny,
                topidla a umístění plynových lahví. Majitelé nemovitostí obdrželi od „preventisty“ kopii
                protokolu s případnými závadami. Tato činnost byla prováděna velmi důsledně - po tuto dobu také v obci nehořelo</li>
            <li>Žňové hlídky existovali do doby než byla pole v okolí přeměněna na pastviny pro krávy.
                Přes den v hlídkách pracovali školáci, v noci je vystřídali členové SDH a dobrovolníci z řad občanů</li>
            <li>Brigádnické hodiny odpracované hasiči pro rozvoj a úklid Radíkova, / akce Z / sběry starého železa</li>
            <li>Soutěžní činnost</li>
            <li>Finanční příspěvky z pokladny hasičů na různé kulturní akce v obci, na charitativní účely</li>
            <li>Nespočet vlastních kulturních akcí, sportovní činnost</li>
            <li>Práce s mládeží, oddíly Plamen, 1974-1989</li>
            <li>Dárcovství krve našich členů</li>
            <li>Konání výročních, výborových a členských schůzí. Ty se konaly většinou v místních hostincích,
                následně v hasičské zbrojnici. Nejvíce projednávanými body bylo placení příspěvků,
                příprava a hodnocení kulturních akcí, přijímání nebo vyloučení členů, vyslání delegátů na různé valné hromady,
                příjem pošty či hodnocení výsledků hasičských soutěží.
                V sedmdesátých a osmdesátých létech pak byly v módě kulturní vložky mladých požárníků.</li>
        </ul>

        <p>Téměř každý zápis končil větou: Jelikož nebylo více volných návrhů, starosta schůzi ukončil.</p>

        <p><b>Chronologický sled významných událostí v životě sboru:</b></p>

        <p>Z vyprávění víme, že krátce po založení sboru byly zapáleny bleskem doškové střechy ve Dvoře,
            hospody u Melcrů a sousedního domu. Současně byla průtrž mračen, proto hasiči přehradili cestu
            od Božích muk a odtud brali vodu na hašení. Podařilo se jim zabránit přeskočení požáru na střechu
            sousedního domu Klimešových. Shořely jen doškové střechy.</p>

        <p>Další požár byl v lese, který se nazýval do té doby „Zadní dubičí“. Trubač na koni objížděl okolní vesnice
            a svolával hasičské sbory k místu požáru, které se od té doby nazývá „Požár“.</p>

        <p>Opět hořel les, tentokrát „Přední dubičí“, v prostoru nad skalkou. Požár uhasili radíkovští hasiči a občané.</p>

        <p>Účast na hašení požáru sladovny v Týnečku, zde nebylo možné použít stříkačku, protože zde nebyl zdroj vody.
            Vyraženým oknem od sklepa protáhli přenosnou stříkačku a čerpali vodu ze sklepní studny.
            Byl zde dostatek vody na uhašení požáru. Dnes na místě sladovny stojí autobusová čekárna.</p>
        <p>Povodeň r.1914 – byl zaplaven celý prostor dnešní točny autobusů včetně domů, ve kterých dnes
            bydlí Schwarzovi, Nádeníčkovi a Rudičtí. Povodeň byla důsledkem průtrže mračen – došlo k ucpání propustku
            pod silnicí. Hasiči pomáhali zachránit majetek lidí z postižených domů.
            Následně voda protrhla silnici a příval se hnal údolím do Lošova kde natropil velké škody.</p>

        <p>1927 slavnost k 35. výročí založení sboru, spojená s cvičením se stroji. Po cvičení se konal výlet na hřišti
            spojený s koulením kuželků v hospodě u Klásků.</p>

        <p>Přijato do pokladny 200 kč od lesní správy za hašení lesního požáru.</p>

        <p>Sbor spravuje vlastní knihovnu.</p>

        <p>1928 několik členů se zúčastnilo Všeslovanského sjezdu v Praze.</p>

        <p>1930 Rolnická záložna na Sv. Kopečku darovala sboru 500 kč.</p>

        <p>1931 Objevila se nová funkce, vzdělavatel, první Šnajdr Antonín.</p>

        <p>Koupě trubky pro trubače, koupě černých blůz za 170 Kč za kus, u p. Králíčka z Bukovan.</p>

        <p>1932 zřížení samaritní /zdravotní/ stráže. Úkolu se ujali bratři Jan Barvíř, František Grauer a Jan Himr.</p>

        <p>1934 Pohřební fond. Byla pro členy založena pohřební pokladna při Okresním výboru v Olomouci.
            Platilo se za úmrtí člena sboru 2 Kč a vybíralo se vždy na 10 pohřbů dopředu.
            Zemřelým členům sboru stojí na poslední cestě čestná stráž.</p>

        <p>Téhož roku se jedna zeď staré zbrojnice u silnice následkem podmočení zřítila, opravu – postavení nové zdi
            provedl stavitel Kullar z Kopečka za 4 000 Kč.</p>

        <p>1936 koupě 20m nových hadic.</p>

        <p>1937 pro urážku vyloučen br. Müller, v srpnu se omluvil a ve sboru setrval.</p>

        <p>1938 hasiči po dlouhých jednáních zakoupili 26.6. motorovou stříkačku. Vybrali si typ 26-krytá, nesundávací,
            s podvozkem dvoukolovým na gumových pneumatikách. Nákup byl uskutečněn přes pana Jurečku ze Samotišek,
            který zastupoval výrobce firmu Stratílek ve Vysokém Mýtě. Tam se také delegace hasičů před nákupem jela podívat.
            Cena stroje byla 19000 kč. vybíralo se i po domech po 50 kč a otevřen úvěr ve spořitelně na Sv. Kopečku.
            Obecní zastupitelstvo rozhodlo, že převezme placení úroků ze nákup motorové stříkačky.</p>

        <p>V době protektorátu činnost sboru stagnovala, přes četné urgence „Obrlandrátu“. Dělalo se jen tolik,
            aby nikdo nebyl stíhán za nesplnění nařízení.</p>

        <p>1942 zřízena el. přípojka na světlo ve zbrojnici za 670 Kč.</p>

        <p>1943 dne 23.4.1943 vznikl v 5:00 požár ve Dvoře v domě Františka Smetany, který se 14 hasičům podařilo zlikvidovat.</p>

        <p>1948 při výletě na střelnici byl lehce postřelen do dlaně na pravé ruce br. Adler František.</p>

        <p>1. 3. 1950 bylo zakoupeno první hasičské auto od sboru závodu MORAVIA Hlubočky za 1000 Kč. V říjnu 1966 v provozu.</p>

        <p>1955-1957 Stavba nové zbrojnice.</p>

        <p>1961 krize v činnosti Sboru, 10. 12. 1961 byla zrušena výroční členská schůze, jelikož se nedostavila
            nadpoloviční většina členů.</p>

        <p>22. 2. 1964 zrušen pohřební fond.</p>

        <p>1967 dne 28. 9. 1967 nové auto z Toveře.</p>

        <p>1968 20. listopadu havarie nového požárního auta ve Chválkovicích, auto odvezeno do šrotu.</p>

        <p>1969 hasiči mají novou motorovou stříkačku a auto T 805.</p>

        <p>1972 požár místní slepičárny na Býčí louce – požár uhašen olomouckými hasiči, místní tři dny hlídali spáleniště</p>

        <p>1974 založen oddíl plamen – jednalo se o mládež a celkem bylo 17 členů</p>

        <p>1975 hromadně byli pořízeny nové uniformy, staré byly nabídnuty k prodeji za 100 Kč za kus,
            bylo předáno našemu sboru nové auto ze Skrbeně.</p>

        <p>1979 byl postaven sušák na hadice vedle zbrojnice, dvě zahoření v listopadu,
            zahoření od televizoru s malou škodou a vypadnutí kouřové roury beze škody.</p>

        <p>1981 pomoc při povodni v Černovíře. Odpracováno při čerpání vody 216 hodin.</p>

        <p>1985 nová siréna.</p>

        <p>1989 přidělena nová stříkačka PPS 12 z Dolopaz, zastavena činnost oddílu Plamen.</p>

        <p>1992 uspořádání oslav u příležitosti 100 let založení SDH Radíkov.</p>

        <p>1994 vznik zásahové jednotky ze členů SDH v rámci města Olomouce.</p>

        <p>1995 nový automobil ARO 240, konání ukázkového cvičení v Radíkově.</p>

        <p>1997 Sbor se intenzivně zapojil do odstraňování následků povodní v Olomouci, obnovení slavností Kácení máje.</p>

        <p>1998 nové hasičské auto Volkswagen transporter, 19. 7. došlo k samovznícení briket v kolně na zahradě u pana Eichlera.
            První zásah provedl majitel s Bednářem Janem ml., zavolal městský Sbor.</p>

        <p>1999 zrušení činnosti preventivních prohlídek komínů hasiči, provádějí kominíci.</p>

        <p>2002 uspořádání oslav u příležitosti 110 let založení Sboru dobrovolných hasičů v Radíkově,
            nová technika: kalové čerpadlo, megafon, světelná rampa – majáky na zásahově vozidlo.
            Finanční dar 5300,- Kč obci Zálezlice postižené srpnovými povodněmi v Čechách.</p>

